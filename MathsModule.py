# Math Module in Python
# Python provides a built-in math module for mathematical operations.
# To access the math function from math module we must import that module at the top

import math #Here module is imported now i can access all the method it will contain

print(math.sqrt(4))         #Square root
print(math.pow(2,3))        #Power
print(math.ceil(.3))        #Round up
print(math.floor(3.1))      #Round down
print(math.factorial(5))    #Factorial
print(math.gcd(4))          #Greatest common divisor
print(math.pi)              #PI
print(math.e)               #Euler
print(math.sin(0))          #Sine
print(math.cos(0))          #Cosine
print(math.tan(0))          #Tangent

 